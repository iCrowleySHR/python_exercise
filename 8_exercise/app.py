# Lê uma linha de entrada do usuário, divide em partes (separadas por espaço),
# converte cada parte para inteiro com map(int, ...) e armazena nas variáveis valor, valor2 e valor3
valor, valor2, valor3 = map(int, input().split())

# Imprime o primeiro valor lido, precedido de um texto explicativo
print("Valor 1:", valor)

# Imprime o segundo valor lido
print("Valor 2:", valor2)

# Imprime o terceiro valor lido
print("Valor 3:", valor3)
