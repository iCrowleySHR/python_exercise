minha_lista = [1, 2, 3]
minha_lista.append(4) # Adicina sempre no final do array
print(minha_lista)
# Saída: [1, 2, 3, 4]

minha_lista = [1, 2, 3]
minha_lista.insert(1, 4)  # O número 4 será inserido na posição 1
print(minha_lista)
# Saída: [1, 4, 2, 3]

minha_lista = [1, 2, 3]
minha_lista.extend([4, 5]) # Adiciona varios valores no final do array
print(minha_lista)
# Saída: [1, 2, 3, 4, 5]
